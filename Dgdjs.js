function isSimilar(input, question, threshold) {
    const commonWords = input.split(" ").filter(word => question.includes(word)).length;
    return commonWords / question.split(" ").length >= threshold;
}

function speakMessage(message) {
    if ('speechSynthesis' in window) {
        const speech = new SpeechSynthesisUtterance(message);
        speech.lang = "ar";
        window.speechSynthesis.speak(speech);
    }
}

function addMessageToChat(message, sender) {
    const chatWindow = document.getElementById("chatWindow");
    const messageBubble = document.createElement("div");
    messageBubble.className = `message ${sender}`;
    messageBubble.innerText = `${sender === "user" ? localStorage.getItem("username") : "HOMOS Ai"}: ${message}`;
    chatWindow.appendChild(messageBubble);
    chatWindow.scrollTop = chatWindow.scrollHeight;
}

